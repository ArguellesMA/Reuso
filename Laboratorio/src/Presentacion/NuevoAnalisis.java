    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        // TODO add your handling code here:
        if(txtsubtotal.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Falta sub total", "error", JOptionPane.ERROR_MESSAGE);
        }else if(txtanticipo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Falta anticipo", "error", JOptionPane.ERROR_MESSAGE);
        }else if(txttotal.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Falta total", "error", JOptionPane.ERROR_MESSAGE);
        }else if(txtarequerimientos.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Falta requerimientos", "error", JOptionPane.ERROR_MESSAGE);
        }else if(txtfecha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Falta fecha", "error", JOptionPane.ERROR_MESSAGE);
        }else if(txthora.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Falta hora", "error", JOptionPane.ERROR_MESSAGE);
        }else if(txtprecio.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Falta precio", "error", JOptionPane.ERROR_MESSAGE);
        }else if(txtideg.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Falta id  del empleado", "error", JOptionPane.ERROR_MESSAGE);
        }else if(txtnomp.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Falta id  del paciente", "error", JOptionPane.ERROR_MESSAGE);
        }else if(txtfolio.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Falta folio", "error", JOptionPane.ERROR_MESSAGE);    
        }
        
        else
        { 
        Negocios.Operaciones na=new Negocios.Operaciones();
         
        try{
        if (na.Guardar(txtsubtotal, txtanticipo, txttotal, cbxanalisis, txtanticipo, txtfecha, txthora, txtprecio, txtideg, txtnomp, txtfolio) ==true)
            JOptionPane.showMessageDialog(null, "Rgistro de un nuevo analisis fue exitoso ");
        else 
            JOptionPane.showMessageDialog(null, "ERROR hay un problema");
        }
        catch(SQLException e)
            {
              System.out.println(" ERROR " + e.getMessage());       
            }      
        
        txtfolio.setText("");
        txtideg.setText("");
        txtnomp.setText("");
        txtsubtotal.setText("");
        txtanticipo.setText("");
        txttotal.setText("");
        txtarequerimientos.setText("");
        txtprecio.setText("");
    }//GEN-LAST:event_GuardarActionPerformed

    }
